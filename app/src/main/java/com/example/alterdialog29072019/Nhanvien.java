package com.example.alterdialog29072019;

public class Nhanvien {
    private String ten;
    private int diachi;

    public Nhanvien(String ten, int diachi) {
        this.ten = ten;
        this.diachi = diachi;
    }

    public String getTen() {
        return ten;
    }

    public void setTen(String ten) {
        this.ten = ten;
    }

    public int getDiachi() {
        return diachi;
    }

    public void setDiachi(int diachi) {
        this.diachi = diachi;
    }
}
